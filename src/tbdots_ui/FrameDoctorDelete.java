    private void btnLoadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoadActionPerformed
        try {

            SQLite.conn = getConnection(SQLite.url);
            SQLite.stmt = SQLite.conn.createStatement();

            //            int id = Integer.parseInt(this.txtUpdateID.getText());
            int id = Integer.parseInt(txtDoctorID.getText());
            java.sql.ResultSet rs = SQLite.stmt.executeQuery("SELECT * FROM Doctor WHERE DOCTORNO="+id+"");
            while(rs.next()){
                this.lastname = rs.getString("LASTNAME");
                this.firstname = rs.getString("FIRSTNAME");
                this.middlename = rs.getString("MIDDLENAME");
                this.contactno = rs.getString("contactNo");
                this.specialization = rs.getString("Specialization");
                this.sex = rs.getString("gender");
                
               

                try{

                }catch(Exception e){

                }
                
                lblFirstname.setText(firstname);
                lblMiddlename.setText(middlename);
                lblLastname.setText(lastname);
                lblContactNo.setText(contactno);
                lblSpecialization.setText(specialization);
                
                if(sex.equals("M")){
                    btnMale.setSelected(true);
                    btnFemale.setSelected(false);
                }else{
                    btnMale.setSelected(false);
                    btnFemale.setSelected(true);
                }
                
            }
            conn.close();
        }
        catch (Exception e) {
            System.out.println("Error: "+e);
        }
                  
    }//GEN-LAST:event_btnLoadActionPerformed

